    public void cargarCB() {
        lista = hi.lanzarConsulta("from Empleados");
        if (!lista.isEmpty()) {
            for (Object e : lista) {
                Empleados emp = (Empleados) e;
                short empNo = emp.getEmpNo();
                jCBempleados.addItem(String.valueOf(empNo));
            }

        } else {

            JOptionPane.showMessageDialog(this, "Error al cargar los dptos", "Error comboDptos", 0);
        }
    }

