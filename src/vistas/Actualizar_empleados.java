    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        if (!jTFndepartamento.getText().isEmpty() && !jTFapellido.getText().isEmpty() && !jTFoficio.getText().isEmpty() && !jTFcomision.getText().isEmpty() && !jTFdir.getText().isEmpty() && !jTFfecha.getDate().toString().isEmpty() && !jTFsalario.getText().isEmpty()) {
            Departamentos d = new Departamentos();
            boolean valido = false;
            lista = hi.lanzarConsulta("from Departamentos");
            if (!lista.isEmpty()) {
                for (Object depart : lista) {
                    Departamentos dep = (Departamentos) depart;
                    byte deptNo = dep.getDeptNo();
                    jCBempleados.addItem(String.valueOf(deptNo));
                    int a = deptNo & 0xFF;
                    if (jTFndepartamento.getText().equals(String.valueOf(a))) {
                        d = dep;
                        valido = true;
                    }
                }
            }
            
            if (valido) {
                float comision = 0, salario = 0;
                if (!jTFcomision.getText().equalsIgnoreCase("null")) {
                    comision = Float.parseFloat(jTFcomision.getText());
                } 
                if (!jTFsalario.getText().equalsIgnoreCase("null")) {
                    salario = Float.parseFloat(jTFsalario.getText());
                }
                
                hi.actualizar(Short.parseShort(jCBempleados.getSelectedItem().toString()), jTFapellido.getText(), jTFoficio.getText(), comision, salario, Short.parseShort(jTFdir.getText()), jTFfecha.getDate(), d);


                cargarCB();
                JOptionPane.showMessageDialog(this, "Empleado actualizado");
            } else {
                JOptionPane.showMessageDialog(null, "El departamento introducido no existe");
            }
            
        } else {
            JOptionPane.showMessageDialog(null, "Faltan datos por rellenar");
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jCBempleadosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jCBempleadosActionPerformed
        List lista2 = hi.lanzarConsulta("from Empleados");
        for (Object e : lista2) {
            Empleados emp = (Empleados) e;
            if (jCBempleados.getSelectedItem().equals(String.valueOf(emp.getEmpNo()))) {
                jTFndepartamento.setText(String.valueOf(emp.getDepartamentos().getDeptNo()));
                jTFapellido.setText(emp.getApellido());
                jTFoficio.setText(emp.getOficio());
                jTFcomision.setText(String.valueOf(emp.getComision()));
                jTFdir.setText(String.valueOf(emp.getDir()));
                jTFfecha.setDate(emp.getFechaAlt());
                jTFsalario.setText(String.valueOf(emp.getSalario()));
            }
        }
    }//GEN-LAST:event_jCBempleadosActionPerformed

