    public void cargarCB() {
        lista = hi.lanzarConsulta("from Departamentos");
        //Comprobamos que no tengamos un objeto vacio
        if (!lista.isEmpty()) {
            //Recorremos las categorias recuperados y las vamos añadiendo al modelo
            for (Object dpto : lista) {
                Departamentos dto = (Departamentos) dpto;
                byte deptNo = dto.getDeptNo();
                jCBdepartamento.addItem(String.valueOf(deptNo));
            }

        } else {

            JOptionPane.showMessageDialog(this, "Error al cargar los dptos", "Error comboDptos", 0);
        }
    }
    
    public void iniciarTabla() {
        tabla = new DefaultTableModel();
        tabla.addColumn("Numero");
        tabla.addColumn("Apellido");
        tabla.addColumn("Oficio");
        tabla.addColumn("Dir");
        tabla.addColumn("Fecha");
        tabla.addColumn("Salario");
        tabla.addColumn("Comision");
        jTable1.setModel(tabla);
    }
    
    public void borrar(){
        tabla.setRowCount(0);
        jTable1.setModel(tabla);
    }

