    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        borrar();
        lista = hi.lanzarConsulta("FROM Empleados where dept_no=" + Short.parseShort(jCBdepartamento.getSelectedItem().toString()));
        if (!lista.isEmpty()) {
            for (Object o : lista) {
                Empleados c = (Empleados) o;
                short empNo = c.getEmpNo();
                Departamentos departamentos = c.getDepartamentos();
                String apellido = c.getApellido();
                String oficio = c.getOficio();
                Short dir = c.getDir();
                Date fechaAlt = c.getFechaAlt();
                Float salario = c.getSalario();
                Float comision = c.getComision();
                tabla.addRow(new Object[]{empNo, apellido, oficio, dir, fechaAlt, salario, comision});
                
            }

        }
        jTable1.setModel(tabla);
    
    }//GEN-LAST:event_jButton2ActionPerformed

